from __future__ import annotations

from datetime import datetime, timezone
import re
from typing import Any
import zlib

try:
    import qrcode
except ImportError:  # pragma: no cover - deployment installs qrcode[pil].
    qrcode = None


PAGE_W = 612
PAGE_H = 792
MARGIN = 54
ACCENT = (255, 130, 0)
INK = (39, 42, 47)
MUTED = (95, 101, 111)
LINE = (218, 222, 229)
SOFT = (247, 248, 250)


def make_receipt_pdf(title: str, rows: list[tuple[str, Any]]) -> bytes:
    notion_url = _notion_url(rows)
    receipt_no = _receipt_no(title, notion_url)
    row_data = [(str(key), _stringify(value)) for key, value in rows]
    content = _content_stream(title, receipt_no, notion_url, row_data)
    qr_image = _qr_image_xobject(notion_url) if notion_url else None
    if not qr_image:
        qr_image = None
    return _build_pdf(content, qr_image)


def _content_stream(title: str, receipt_no: str, notion_url: str, rows: list[tuple[str, str]]) -> bytes:
    ops: list[str] = []
    _fill_rect(ops, 0, PAGE_H - 96, PAGE_W, 96, (51, 54, 58))
    _fill_rect(ops, 0, PAGE_H - 100, PAGE_W, 4, ACCENT)
    _text(ops, "CAMM Sample Tracking", MARGIN, 744, 10, (255, 255, 255), font="F2")
    _text(ops, _truncate(title, 42), MARGIN, 719, 20, (255, 255, 255), font="F2")
    _text(ops, "Submission receipt", MARGIN, 699, 10, (235, 238, 242))

    if notion_url:
        ops.append("q")
        ops.append("84 0 0 84 474 682 cm")
        ops.append("/Im1 Do")
        ops.append("Q")
        _text(ops, "Scan to open", 484, 670, 8, (235, 238, 242))

    _fill_rect(ops, MARGIN, 628, PAGE_W - (MARGIN * 2), 46, SOFT)
    _stroke_rect(ops, MARGIN, 628, PAGE_W - (MARGIN * 2), 46, LINE)
    _text(ops, "Receipt", 72, 654, 9, MUTED, font="F2")
    _text(ops, receipt_no, 72, 638, 12, INK, font="F2")
    _text(ops, "Generated", 300, 654, 9, MUTED, font="F2")
    _text(ops, datetime.now(timezone.utc).strftime("%Y-%m-%d %H:%M UTC"), 300, 638, 12, INK, font="F2")

    y = 588
    _text(ops, "Submission Details", MARGIN, y, 14, INK, font="F2")
    y -= 18
    _stroke_line(ops, MARGIN, y, PAGE_W - MARGIN, y, LINE)
    y -= 22

    for label, value in rows:
        if not value:
            continue
        wrapped = _wrap(value, 68)
        block_h = max(28, 14 + len(wrapped) * 13)
        if y - block_h < 72:
            _text(ops, "Additional fields omitted from this one-page receipt.", MARGIN, y, 9, MUTED)
            break
        _fill_rect(ops, MARGIN, y - block_h + 6, PAGE_W - (MARGIN * 2), block_h, (255, 255, 255))
        _stroke_line(ops, MARGIN, y - block_h + 6, PAGE_W - MARGIN, y - block_h + 6, LINE)
        _text(ops, _truncate(label, 24), MARGIN, y, 9, MUTED, font="F2")
        value_y = y
        for line in wrapped[:4]:
            _text(ops, line, 190, value_y, 10, INK)
            value_y -= 13
        y -= block_h

    _fill_rect(ops, 0, 0, PAGE_W, 42, SOFT)
    _stroke_line(ops, 0, 42, PAGE_W, 42, LINE)
    _text(ops, "Generated by the Notion Sample Tracker web forms", MARGIN, 18, 8, MUTED)
    return "\n".join(ops).encode("latin-1", "replace")


def _build_pdf(content: bytes, qr_image: dict[str, Any] | None) -> bytes:
    resources = b"<< /Font << /F1 4 0 R /F2 5 0 R >>"
    objects = [
        b"<< /Type /Catalog /Pages 2 0 R >>",
        b"<< /Type /Pages /Kids [3 0 R] /Count 1 >>",
        None,
        b"<< /Type /Font /Subtype /Type1 /BaseFont /Helvetica >>",
        b"<< /Type /Font /Subtype /Type1 /BaseFont /Helvetica-Bold >>",
        _stream_object(content),
    ]
    if qr_image:
        resources += b" /XObject << /Im1 7 0 R >>"
        objects.append(_image_object(qr_image))
    resources += b" >>"
    objects[2] = (
        b"<< /Type /Page /Parent 2 0 R /MediaBox [0 0 612 792] /Resources "
        + resources
        + b" /Contents 6 0 R >>"
    )

    pdf = bytearray(b"%PDF-1.4\n")
    offsets = [0]
    for index, obj in enumerate(objects, start=1):
        offsets.append(len(pdf))
        pdf.extend(f"{index} 0 obj\n".encode("ascii"))
        pdf.extend(obj or b"")
        pdf.extend(b"\nendobj\n")
    xref = len(pdf)
    pdf.extend(f"xref\n0 {len(objects) + 1}\n".encode("ascii"))
    pdf.extend(b"0000000000 65535 f \n")
    for offset in offsets[1:]:
        pdf.extend(f"{offset:010d} 00000 n \n".encode("ascii"))
    pdf.extend(f"trailer\n<< /Size {len(objects) + 1} /Root 1 0 R >>\nstartxref\n{xref}\n%%EOF\n".encode("ascii"))
    return bytes(pdf)


def _qr_image_xobject(value: str) -> dict[str, Any]:
    if qrcode is None:
        return {}
    image = qrcode.make(value).convert("RGB").resize((168, 168))
    raw = image.tobytes()
    return {"width": image.width, "height": image.height, "data": zlib.compress(raw)}


def _image_object(image: dict[str, Any]) -> bytes:
    data = image["data"]
    header = (
        f"<< /Type /XObject /Subtype /Image /Width {image['width']} /Height {image['height']} "
        f"/ColorSpace /DeviceRGB /BitsPerComponent 8 /Filter /FlateDecode /Length {len(data)} >>\n"
    ).encode("ascii")
    return header + b"stream\n" + data + b"\nendstream"


def _stream_object(content: bytes) -> bytes:
    return b"<< /Length " + str(len(content)).encode("ascii") + b" >>\nstream\n" + content + b"\nendstream"


def _text(ops: list[str], text: str, x: int, y: int, size: int, color: tuple[int, int, int], font: str = "F1") -> None:
    r, g, b = _rgb(color)
    ops.append("BT")
    ops.append(f"{r:.3f} {g:.3f} {b:.3f} rg")
    ops.append(f"/{font} {size} Tf")
    ops.append(f"1 0 0 1 {x} {y} Tm")
    ops.append(f"({_escape_pdf(text)}) Tj")
    ops.append("ET")


def _fill_rect(ops: list[str], x: int, y: int, w: int, h: int, color: tuple[int, int, int]) -> None:
    r, g, b = _rgb(color)
    ops.append(f"{r:.3f} {g:.3f} {b:.3f} rg")
    ops.append(f"{x} {y} {w} {h} re f")


def _stroke_rect(ops: list[str], x: int, y: int, w: int, h: int, color: tuple[int, int, int]) -> None:
    r, g, b = _rgb(color)
    ops.append(f"{r:.3f} {g:.3f} {b:.3f} RG")
    ops.append(f"{x} {y} {w} {h} re S")


def _stroke_line(ops: list[str], x1: int, y1: int, x2: int, y2: int, color: tuple[int, int, int]) -> None:
    r, g, b = _rgb(color)
    ops.append(f"{r:.3f} {g:.3f} {b:.3f} RG")
    ops.append(f"{x1} {y1} m {x2} {y2} l S")


def _rgb(color: tuple[int, int, int]) -> tuple[float, float, float]:
    return tuple(channel / 255 for channel in color)


def _wrap(text: str, width: int) -> list[str]:
    words = text.split()
    if not words:
        return [""]
    lines: list[str] = []
    current = words[0]
    for word in words[1:]:
        if len(current) + len(word) + 1 > width:
            lines.append(current)
            current = word
        else:
            current += " " + word
    lines.append(current)
    return lines


def _stringify(value: Any) -> str:
    if isinstance(value, list):
        return ", ".join(str(item) for item in value if str(item))
    return str(value or "")


def _notion_url(rows: list[tuple[str, Any]]) -> str:
    for key, value in rows:
        if str(key).lower() == "notion url":
            return str(value or "").strip()
    return ""


def _receipt_no(title: str, notion_url: str) -> str:
    source = notion_url or title or datetime.now(timezone.utc).isoformat()
    cleaned = re.sub(r"[^A-Za-z0-9]+", "", source)
    return (cleaned[-10:] or "receipt").upper()


def _truncate(text: str, limit: int) -> str:
    return text if len(text) <= limit else text[: limit - 1] + "..."


def _escape_pdf(value: str) -> str:
    value = re.sub(r"[\r\n\t]+", " ", value)
    return value.replace("\\", "\\\\").replace("(", "\\(").replace(")", "\\)")
